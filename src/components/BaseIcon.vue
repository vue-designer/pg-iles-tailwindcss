<script setup lang="ts">
  import { computed } from 'vue'

  const props = defineProps({
    name: {
      type: String,
      default: 'line-md:iconify1',
    },
    width: {
      type: String,
      default: '',
    },
    height: {
      type: String,
      default: '',
    },
  })

  const addUnit = (val: any) => {
    if (val && !(val.endsWith('px') || val.endsWith('em'))) {
      return `${val}px`
    } else {
      return val
    }
  }

  const iconStyle = computed(() => {
    const width = addUnit(props.width) || addUnit(props.height) || '16px'
    const height = addUnit(props.height) || width
    return {
      width,
      height,
    }
  })
</script>

<template>
  <div :class="name" :style="iconStyle"></div>
</template>

